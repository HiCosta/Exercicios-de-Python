import math
ang = float(input('Digite um 창ngulo: '))
seno = math.sin(math.radians(ang))
print('O 창ngulo {} tem o seno {:.2f}'.format(ang, seno))
cos = math.cos(math.radians(ang))
print('O 창ngulo {} tem o cosseno {:.2f}'.format(ang, cos))
tan = math.tan(math.radians(ang))
print('O 창ngulo {} tem a tangente {:.2f}'.format(ang, tan))
